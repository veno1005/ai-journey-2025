def calculator():
    print("üßÆ Welcome to CLI Calculator!")

    while True:
        try:
            num1 = float(input("Enter first number: "))
            operator = input("Enter operator (+, -, *, /): ")
            num2 = float(input("Enter second number: "))

            if operator == "+":
                result = num1 + num2
            elif operator == "-":
                result = num1 - num2
            elif operator == "*":
                result = num1 * num2
            elif operator == "/":
                if num2 == 0:
                    raise ZeroDivisionError
                result = num1 / num2
            else:
                print("‚ùå Invalid operator!")
                continue

            print("‚úÖ Result:", result)

        except ValueError:
            print("‚ö†Ô∏è Please enter numbers only!")
        except ZeroDivisionError:
            print("‚ö†Ô∏è Cannot divide by zero!")

        choice = input("Do you want to calculate again? (y/n): ").lower()
        if choice != "y":
            print("Goodbye!")
            break

calculator()